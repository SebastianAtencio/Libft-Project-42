#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	b;
	int		a;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else
	{
		a = n;
		if (a < 0)
		{
			write(fd, "-", 1);
			a = -a;
		}
		if (a > 9)
		{
			ft_putnbr_fd(a / 10, fd);
			a %= 10;
		}
		b = a + '0';
		write(fd, &b, 1);
	}
}
